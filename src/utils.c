#include "utils.h"

void	ft_bzero(void *ptr, size_t size)
{
	while (size & 0b111)
	{
		--size;
		*((char *)ptr + size) = 0;
	}
	while (size >= 8)
	{
		size -= 8;
		*((long *)ptr + (size >> 3)) = 0;
	}
}
